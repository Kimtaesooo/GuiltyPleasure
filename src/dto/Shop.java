package dto;

public class Shop {
	private String s_itemcode;
	private String s_itemname;
	private int s_price;
	private int s_deadline;
	private int s_limit_num;
	private int s_limit_pow;
	private String s_content;
	
	public String getS_itemcode() {
		return s_itemcode;
	}
	public void setS_itemcode(String s_itemcode) {
		this.s_itemcode = s_itemcode;
	}
	public String getS_itemname() {
		return s_itemname;
	}
	public void setS_itemname(String s_itemname) {
		this.s_itemname = s_itemname;
	}
	public int getS_price() {
		return s_price;
	}
	public void setS_price(int s_price) {
		this.s_price = s_price;
	}
	public int getS_deadline() {
		return s_deadline;
	}
	public void setS_deadline(int s_deadline) {
		this.s_deadline = s_deadline;
	}
	public String getS_content() {
		return s_content;
	}
	public void setS_content(String s_content) {
		this.s_content = s_content;
	}
	public int getS_limit_num() {
		return s_limit_num;
	}
	public void setS_limit_num(int s_limit_num) {
		this.s_limit_num = s_limit_num;
	}
	public int getS_limit_pow() {
		return s_limit_pow;
	}
	public void setS_limit_pow(int s_limit_pow) {
		this.s_limit_pow = s_limit_pow;
	}
	
	
}
